    public function search ( Request $request )
    {

        $q = '%' . str_replace( ' ', '%', trim( $request->get( 'query' ) ) ) . '%';

        $addresses = Address
            ::where( 'name', 'like', $q )
            ->orderBy( 'name' )
            ->paginate( 30 );

        return $addresses;

